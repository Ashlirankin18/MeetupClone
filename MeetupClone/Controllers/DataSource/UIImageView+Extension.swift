import UIKit

extension UIImage {
    
    /// The image representing a user is not a member of the group
    static let notAGroupMember = UIImage(named: "notAMember")
    
    /// The image representing an no events
    static let noEventsFound = UIImage(named: "noEvents")
   
    /// The image representing a no favorites
    static let noFavoritesFound = UIImage(named: "noFavorite")
    
    /// The image representing no groups found
    static let noGroupsFound = UIImage(named: "noGroups")
   
    /// The image representing no internet connection.
    static let noInternetConnection = UIImage(named: "noInternet")
}
